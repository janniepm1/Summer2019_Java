package day03_variables;

public class Warmup {
	public static void main(String[] args) {
		
		
		double cucumber=2.5;
		int tomatoes=3;
		double onion =1.5;
		int pepper=2;
		int carrot=4;
		
		
		onion=onion*2;
		tomatoes=tomatoes*4;
		pepper=pepper*1;
		carrot=carrot*6;
		
		
		System.out.println("1 cucumber is:" +cucumber);
		System.out.println("4 tomatoes is:" +tomatoes);
		System.out.println("2 onion is:" +onion);
		System.out.println("1 pepper is :"+pepper);
		System.out.println("6 carrot is:"+carrot);
	}

}
